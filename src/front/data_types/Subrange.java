package front.data_types;

public class Subrange {
    private int val1;
    private int val2;

    public Subrange(int val1, int val2) {
        this.val1 = val1;
        this.val2 = val2;
    }

    public int getVal1() {
        return val1;
    }

    public int getVal2() {
        return val2;
    }
}
